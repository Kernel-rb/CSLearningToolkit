
// /POP
let fruits = ['pomme', 'banane', 'orange'];
let dernierFruit = fruits.pop()
console.log(dernierFruit);
console.log(fruits);

// PUSH
const animaux = ['chien', 'chat'];
let nvLongueuer = animaux.push('souris', 'oiseau')
console.log(nvLongueuer);
console.log(animaux);

// SHIFT

const villes = ['Madrid', 'Paris', 'Londres'];
let firstVille = villes.shift();
console.log(firstVille);
console.log(villes);

// UNSHIFT
let langages = ['HTML', 'CSS', 'Javascript'];
let nouvelleLongueur = langages.unshift('Java', 'C++');
console.log(nouvelleLongueur);
console.log(langages);