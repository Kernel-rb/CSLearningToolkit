fn main() {
    let language = "Rust";
    println!("I am learning {} programming language", language);
}
fn multiple() {
    Let (var , var1) =  ("Rust", "Python");
    println!("I am learning {} and {} programming languages", var, var1);
}
fn mutable() {
    let mut language = "Rust";
    println!("I am learning {} programming language", language);
    language = "Python";
    println!("I am learning {} programming language", language);
}
